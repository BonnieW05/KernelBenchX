import os
import sys
import subprocess
from multiprocessing import Pool, Lock, Value
from tqdm import tqdm
import signal

script_dir = os.environ.get("KERNELBENCHX_SCRIPT_DIR", "./tmp")
log_dir = os.environ.get("KERNELBENCHX_LOG_DIR", "./logs")

_visible_gpus_raw = os.environ.get("CUDA_VISIBLE_DEVICES")
if _visible_gpus_raw is not None and _visible_gpus_raw.strip() != "":
    _visible_gpu_ids = [x.strip() for x in _visible_gpus_raw.split(",") if x.strip() != ""]
else:
    _visible_gpu_ids = None

_default_gpu_count = len(_visible_gpu_ids) if _visible_gpu_ids is not None else 8
gpu_count = int(os.environ.get("KERNELBENCHX_GPU_COUNT", _default_gpu_count))
if _visible_gpu_ids is not None:
    gpu_count = min(gpu_count, len(_visible_gpu_ids))

timeout = int(os.environ.get("KERNELBENCHX_BENCH_TIMEOUT", 180))  # configurable per-script timeout (seconds)

# Only run golden perf scripts; skip the cleaned operator implementation *.py files
# generated by write_file.py to avoid redundant subprocess launches and wasted time
# that could cause subsequent *_perf scripts to time out.
scripts = sorted(
    f
    for f in os.listdir(script_dir)
    if f.endswith(".py") and f.endswith("_perf.py")
)
scripts = [os.path.join(script_dir, script) for script in scripts]
total_scripts = len(scripts)

progress = Value('i', 0)
progress_lock = Lock()

def run_script(args):
    gpu_id, script = args

    script_name = os.path.basename(script)
    log_file = os.path.join(log_dir, f"{script_name}.log")
    err_file = os.path.join(log_dir, f"{script_name}.err")

    if _visible_gpu_ids is not None:
        assigned_gpu = _visible_gpu_ids[gpu_id]
    else:
        assigned_gpu = str(gpu_id)

    results_path = os.environ.get("KERNELBENCHX_RESULTS_PATH", "./results")
    py = sys.executable
    cmd = f"CUDA_VISIBLE_DEVICES={assigned_gpu} KERNELBENCHX_RESULTS_PATH={results_path} {py} {script}"

    timed_out = False
    return_code = None
    with open(log_file, "w") as log, open(err_file, "w") as err:
        process = subprocess.Popen(cmd, shell=True, stdout=log, stderr=err, preexec_fn=os.setsid)
        try:
            process.wait(timeout=timeout)
            return_code = process.returncode
        except subprocess.TimeoutExpired:
            timed_out = True
            try:
                err.write(f"TIMEOUT: killed {script_name} after {timeout} seconds\n")
                err.flush()
            except Exception:
                pass
            try:
                os.killpg(os.getpgid(process.pid), signal.SIGKILL)
            except Exception:
                process.kill()
            tqdm.write(f"⏱️ timeout: killed {script_name} ({timeout} s)")

    # Log non-zero exit codes (not timeout) so silent failures are visible
    if not timed_out and return_code is not None and return_code != 0:
        try:
            with open(err_file, "a") as err:
                err.write(f"\n[EXIT CODE: {return_code}]\n")
        except Exception:
            pass
        tqdm.write(f"❌ failed {script_name} (exit code {return_code})")

    with progress_lock:
        progress.value += 1
        status = "⏱️" if timed_out else ("❌" if return_code != 0 else "✅")
        tqdm.write(f"{status} finished {progress.value}/{total_scripts}: {script_name}")

os.makedirs(log_dir, exist_ok=True)

if __name__ == "__main__":
    with Pool(processes=gpu_count) as pool, tqdm(total=total_scripts, desc="Process", ncols=80) as pbar:
        args_list = [(i % gpu_count, scripts[i]) for i in range(total_scripts)]
        
        for _ in pool.imap(run_script, args_list):
            pbar.update(1)

        pool.close()
        pool.join()